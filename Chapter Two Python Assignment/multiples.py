# if 1024 divided by 4 gives remainder print it as as a multiple of 4,
# if not, it is not a multiple
# if 2 can divide 10without remainder, it is a multiple of 10, if not
# it is not a multiple of 10


if(1024 % 4 == 0):

	print("1024 is a multiple of 4")
else:
	print("1024 is not a multiple of 4")

if(2 % 10 == 0):

	print("2 is a multiple of 10")

else:
	print("2 is not a multiple of 10")
