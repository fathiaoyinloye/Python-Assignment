investment = 1000

return_rate = 7/100

number_of_years1 = 10

number_of_years2 = 20

number_of_years3 = 30


deposit_years1 = investment * (1 + return_rate )**number_of_years1 
deposit_years2 = investment * (1 + return_rate )**number_of_years2 
deposit_years3 = investment * (1 + return_rate )**number_of_years3 


print("Investment return on 1000 after 10 years is", deposit_years1)

print("Investment return on 1000 after 20 years is", deposit_years2)

print("Investment return on 1000 after 30 years is", deposit_years3)
